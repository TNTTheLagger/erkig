    id INT PRIMARY KEY AUTO_INCREMENT,
    city_from INT,
    city_to INT,
    distance FLOAT,
    FOREIGN KEY (city_from) REFERENCES cities(id),
    FOREIGN KEY (city_to) REFERENCES cities(id)
);

